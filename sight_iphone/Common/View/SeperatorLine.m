#import "SeperatorLine.h"
 
 
@implementation SeperatorLine

- (void)awakeFromNib {
    self.backgroundColor = [UIColor qunarGrayColor];
    [self setViewHeight:kLineDiameter];
    [self relayout];
}

- (void)relayout {
    [self setViewWidth:self.superview.frame.size.width];
    [self setViewY:self.superview.frame.size.height - kLineDiameter];
}

@end
